package com.fixmyschool;

public class UserRegForm {
    public String userName, userSurname, phoneNumber, email, password;




    public UserRegForm(String userName, String userSurname, String phoneNumber, String email, String password) {
        this.userName = userName;
        this.userSurname = userSurname;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.password = password;
    }
}
